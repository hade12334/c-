using System;

namespace homework_4._12
{
    class Date
    {
        public int Month { get; set; }
        public int Day { get; set; }
        public int Year { get; set; }

        public void DisplayDate() {
            Console.WriteLine($"{Month}/{Day}/{Year}");
        }

        public Date(int month, int day, int year)
        {
            Month = month;
            Day = day;
            Year = year;
        }
    }
}
